void main()
{
  int a = 27;
  int b = 20;

  print("Arethmatic Operation");

  int sum = a+b;
  int abb = a-b;
  double div = a/b;
  int mul = a*b;
  int per = a%b;

  print("$a + $b =  $sum");
  print("$a - $b = $abb");
  print("$a / $b = $div");
  print("$a * $b = $mul");
  print("$a % $b = $per");


}
